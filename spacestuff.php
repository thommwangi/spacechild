                <div class="content">
                        <h2>Space Travel</h2>
                        <p>The space age began in 1957 when the Soviet Union launched Sputnik 1, 
                            the world's first man-made satellite. Also in 1957, Sputnik 2 was launched 
                            carrying a dog named Laika. Laika was the first animal to orbit the Earth. 
                            In 1961, Yuri Gagarin became the first human to pilot a spacecraft when he 
                            was launched aboard Vostok 1 by the Soviet Union. Later that year, Alan 
                            Shepard became the first American to pilot a spacecraft. He was followed
                             by John Glenn, who orbited the Earth three times in 1962.
                             In 1963, the United States and the Soviet Union started a race to see
                              who could put a man on the Moon first. In 1969, the United States won
                               when Neil Armstrong and Buzz Aldrin landed the Lunar Module Eagle on
                                the Moon's surface.</p>
