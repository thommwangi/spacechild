                <div class="content">
                        <h2>Space Wardrobe</h2>
                        <p>Astronauts need special clothes to protect them so they can live and work in space.
                             In fact, astronauts need a lot of clothes! They have a special suit they wear
                              during launch and re-entry. It protects them from the changes in pressure that
                               happen when you come and go from space. This suit has a helmet, gloves,
                                and boots. Once in orbit, they wear shirts and pants that are just like 
                                the clothes we wear here on the ground. To go outside their spacecraft 
                                while they are in orbit, they have a special suit called an "extravehicular 
                                mobility unit", or EMU. It has a way to keep the astronaut cool, a
                                 headphone/microphone so the astronaut can talk to the Shuttle, oxygen 
                                 for breathing, and water to drink. If they want to move freely through
                                  space, they wear a special backpack called the "manned maneuvering unit" 
                                  or MMU.</p>
