                        <h2>Hubble Space Telescope</h2>
                        <p>The Hubble Space Telescope was launched on the Space Shuttle on April 24, 
                            1990. It is a very special telescope which orbits far above the Earth.
                             Hubble allows us to see objects more clearly because it is above the 
                             clouds in Earth's atmosphere. These clouds block the view of the 
                             Earth-based telescopes which scientists have used for many years. 
                             Hubble is not only a powerful telescope, but it also carries cameras
                              which allow scientists on Earth to see objects billions of light-years away.
                               Hubble runs on solar power. Scientists in Greenbelt, Maryland control 
                               it using radio signals. The Hubble Space Telescope was built to last for 
                               at least 15 years. Astronauts from the Space Shuttle make visits to Hubble 
                               so they can give it a "check-up". Everyone wants to keep Hubble in perfect 
                               working condition so it will continue to send back amazing pictures to us 
                               here on Earth.</p>
                    </div>
