                      and have a college degree. Most future astronauts take a lot of science 
                      and math. It is very hard, and sometimes very dangerous, to be an astronaut.
                        Once selected to join the astronaut corps, training begins at the Johnson
                         Space Center in Houston, Texas. Astronauts practice all of the jobs they
                          will need to know during their space mission. They practice these jobs 
                          many, many times. Some astronauts learn to drive the Space Shuttle. 
                          Some are trained to perform jobs like science experiments or spacewalks.</p>
